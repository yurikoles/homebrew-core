  license "Apache-2.0"
  head "https://github.com/keploy/keploy.git", branch: "main"

  bottle do
    sha256 cellar: :any_skip_relocation, arm64_tahoe:   "f4a0b72e6238183b9a9637216db98ef77eae1385094e0a4557cc38147ddf4713"
